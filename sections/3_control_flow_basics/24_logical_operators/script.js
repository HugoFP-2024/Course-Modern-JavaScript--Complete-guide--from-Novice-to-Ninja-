// Logical Operators in JavaScript:
// Logical operators are used to combine or invert boolean values and expressions.
// The main logical operators are:
// - && (AND): Returns true if both expressions are true.
// - || (OR): Returns true if at least one expression is true.
// - ! (NOT): Inverts the value of a boolean expression (true becomes false, false becomes true).
// These operators are commonly used in conditional statements and loops.
