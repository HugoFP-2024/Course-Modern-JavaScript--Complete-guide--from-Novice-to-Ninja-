// Numbers
// Math Operators: +, -, *, /, **, %

// NaN - Not a Number: It is the result of a calculation that does not result in a number.

let circleRadius = 10;
const pi = 3.14;

// Shorthand Operators:

console.log(++circleRadius);     
// This is the same as 'circleRadius = circleRadius + 1', also works with subtraction 
// Note: Using the '++' or other operator before OR after the variable actually makes a difference!

console.log(circleRadius += 10)  
// Takes the current value of 'circleRadius' and adds 10. Also works with other operators like -, *, /, %